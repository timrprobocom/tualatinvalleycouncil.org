<h2>TVC Recognition Award</h2>

<p>Each year the TVC Recognition Award is presented to an individual, or couple, who has made a significant contribution to the square dancing community.</p>

<p>We are pleased to announce the 2016 TVC Recognition Award goes to 
<B>Nan Overall</B>.
</p>

<img style='padding-right: 5px; float: left' src='images/Overall.JPG'>

<p>Nan is a member of three TVC clubs: Eager Beavers, Toe Draggers, and Sunset Promenaders.   She is very intense on the dance floor.
She is an army of one making people get up and dance.  I have discovered her generous spirit and willingness to help with the work to be done too.
She has a powerful look, a gaze that makes you want to do right.  One TVC dance she helped with split the pot, what a collection that night,
apparently we would rather cough up five bucks than feel the heat of the stare.  We are happy she uses her power for good.

<p>In her clubs, Nan has been a loyal member attending all dances, lessons, and club meetings.  She has affectionately earned the nickname of "The Cake Lady".
<p>As an Eager Beaver, Nan has been recognized for her contributions to the club with the Swenson Award.  She is known for her photography at club events, and publishing stories and pictures about the club in the Beaver Log.
<p>The TVC recognizes in Nan a true desire to get people dancing.  Her persistence in rounding up people to complete squares is attested to by all who know her.
<p>With thanks and gratitude we offer Nan our sincerest congratulations on displaying a spirit of service in pursuit of square dance.

