<h2>TVC Recognition Award</h2>

<p>Each year the TVC Recognition Award is presented to an individual, or couple, who has made a significant contribution to the square dancing community.</p>

<p>We are pleased to announce the 2014 TVC Recognition Award goes to 
<B>Dale and Kathy Worthington</B>.
</p>

<!--img style='padding-right: 5px; float: left' src='images/clendenin.jpg' -->
<p>
Dale &amp; Kathy started dancing in 1972.  Dale was in the Army and they
danced in numerous states as well as in Belgium &amp; Germany.
<p>
In 1992 they became very involved with the Boy Scouts and held numerous
positions as they moved around.  They are still active with the area
Cub Scout Day Camp Program and have been awarded the Silver Beaver.
Dale still makes craft items for the Summer Camp.
<p>
That same year they took square dance lessons again to remove the rust.
They then joined the Hayshakers in Astoria/Seaside.  They have been
Club President at least 6 years apiece along with many other offices.
They served on the Sunset Area Council as Delegate and Secretary.
They represented the Council as delegates to the State Federation.
<p>
Dale &amp; Kathy have been Oregon Federation Delegates to the USDA for
the past 6 years.   They served on the boards for the 2005 and 2012 National 
Conventions.
<p>
They have been co-Chairs for the Seaside Sashay for 13 years.  They have
been very active in their club and Council.  Kathy is currently the TVC
State Delegate.  For the last 7 years they have been Grange Officers to
ensure that the Grange stays open and the club has a place to dance.
<p>
Dale has been State Federation Recording Secretary, 2nd Vice-President 
followed by 1st Vice-President and currently State Federation, President.
<p>
It's my pleasure to award this TVC Presidents Recognition Plaque to 
this deserving couple, Dale &amp; Kathy Worthington.

