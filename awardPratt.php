<h2>TVC Recognition Award</h2>

<p>Each year since 1994, the TVC Recognition Award is presented to an individual or 
couple who has made a significant contribution to the square dancing community.</p>

<p>We are pleased to announce the 2019 TVC Recognition Award goes to 
<b>Ken &amp; Dianne Pratt</b>.
</p>

<img style='padding-right: 5px; float: left' src='https://ofn.club/content/2015-02/t300/Pratt-Ken-Dianne.png'>

<p>
Congratulations to Ken and Dianne Pratt. Ken and Dianne have received
this award because of many things, but I feel that they deserve this
special award because of all of the work they have done to make round
dance a special place in our lives. I feel they have gone beyond just
being instructors of round dance, they have become a part of our lives
in so many ways. I am so very sorry that Ken is not able to be here to
share this with Dianne, but I know he is looking down on us right now,
and is proud to have been a big part of this group of people tonight
and a big part of those lives that they have touched.

<p>
Ken and Dianne have been square and round dancing together for many years,
in fact, since the early 1990s, but they started their career as cuers in
2003. Ken and Dianne have been partners this whole time, and they have
taught hundreds, if not thousands of people to round dance, from Phase
II to some of the highest levels of round dance. They have traveled
all over the state to promote and teach round dancing. They have been
involved in most of the festivals that we have, along with being very
involved with the junior dancers. They have always been willing to be
part of what’s going on with the TVC and the clubs all over the state.

<p>
My wife and I have only known Ken and Dianne for about four years, but
in that time period they have been a very important part of our lives
within the club and the lessons that we stumbled through. I observed that
both of them have been about making us better dancers and are willing to
take the extra time to teach beyond just classes. My personal experience
has been even though I can’t tell which direction to go next, they
haven’t given up on me.

<p>
Ken and Dianne have taken many of us out on the floor, and made us feel
like there’s a chance we might be able to learn this. They have done
this because they care and want people to love it as much as they do,
and we thank them for that.

<p>
Ken and Dianne have promoted round dance all over the state, and are
well known for their abilities to teach all levels of round dance, and
many times have showcased what it really should look like on the floor.

<p>
You must know that most of this was written before Ken passed. So it
seems as though he is here with us, he is, and we will not forget what
Ken and Dianne have done for us. I do not want to make this a sad moment,
but there is going to be a big void in our club and in the round dance
community with Ken gone, and I hope that Dianne will stay a big part of
our lives to help fill that void. Some of us are just square dancers,
and some of us are just round dancers, and some of us are both. But I
think that we have all been touched by Ken and Dianne at one time or
another. So I just want to say thank you for what you have done for us."


