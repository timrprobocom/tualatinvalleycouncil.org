<h2>TVC Recognition Award</h2>

<p>Each year since 1994, the TVC Recognition Award is presented to an individual or 
couple who has made a significant contribution to the square dancing community.</p>

<p>We are pleased to announce the 2021 TVC Recognition Award goes to 
<b>Darrell &amp; Erin Kalmbach</b>.
</p>

<img style='padding-right: 5px; float: left' src='/files/Kalmbach-D-E.png'>

<p> 
This year’s TVC Recognition award went to a couple that has been a
big part of square and round dance for very long time. We are very Blessed
to have a special couple that are devoted to supporting square and round
dance and they work very hard to promoting our sport.  Darrell came from a
square dance family, the family danced with the Buzzin Bees in Vancouver.

<p> 
He was not allowed to dance when he first wanted to because he was
too young, so he has only been dancing since 1965. He sharpened his
skill by dancing in a youth club which included competition dancing.

<p> 
Darrell started calling in 1970. His first clubs were the Noble
8’s and the Hoedowners.
He currently is caller for the Promenaders Plus, The River City
Dancers,  and the Eager Beavers Plus. Along with many other gigs he does
in his spare time.

<p> 
Darrell and Erin’s passion for this sport has huge impact on our
clubs in the TVC. They have a huge following, and you can guarantee they
bring that to the dance.  They put the "fun" in Dance!

<p> 
Erin, who makes this a complete team, has been a big part of square
and round dancing in her own right. She not only makes sure Darrell
is in the right place at the right time, but she is a big part of our
council as a delegate from the PAC. Keeping both councils aware of what
the other is doing. Along with that she will take on anything that needs
done, including Door duty and staying and cleaning after the dance. That
makes her a big part of every club they are associated with.

<p> 
I think you will agree that they are deserving of this award, and
I am proud to be the one to present this to them.

