<h2>Banner Etiquette</h2>

<p>
A &ldquo;Banner Raid&rdquo; is an old square dancing tradition, used to promote
the visitation of square dancing clubs with each other.

<p>
For instance, if &ldquo;Club A&rdquo; has a sufficient number of dancers and visit
&ldquo;Club B&rdquo;, Club A may &ldquo;win or steal&rdquo; Club B's visitation/traveling
banner.  Subsequently, Club B may (1) retrieve or recover their own
club's traveling banner or (2) &ldquo;steal&rdquo; one of Club A's banners
when it makes a visit with a sufficient number of members to Club A.

<p>
Visitation (or) Traveling Banner guidelines vary widely across the
United States as well as within the Tualatin Valley Council (TVC).
Also, not all TVC clubs participate in this tradition.

<p>
Therefore, the TVC recommends that all TVC clubs place a statement
on their website informing visitors of their Traveling/Visitation
Banner policy, so that this information is available to those
interested. TVC also recommends that visiting clubs telephone or email
the club-to-be-visited prior to their arrival, in order for the visited
club to be prepared for the additional dancers coming.

