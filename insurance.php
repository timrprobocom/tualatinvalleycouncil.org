<h2>Liability and Accidental Medical Coverage</h2>

<p>The United Square Dancers of America (USDA) Insurance Program entitles
your square dance club and its members, to liability and accident medical
insurance protection, while participating in scheduled and sponsored
dancing activities.

<p>The Liability Insurance protects your club, and its members, from
financial loss due to unforeseen incidents including bodily injury and
property damage, that may develop into litigation up to $1,000,000.

<p>The Accident Medical Insurance covers individuals up to the amount
of $10,000, and only applies after a member's primary medical insurance
has been used.

<p>The USDA Dancers Insurance Program requires all members to be insured
and is due at the end of October every year.

<p>More details can be found by downloading the 
<a href="files/insflyer.pdf" title="Click to download brochure">USDA Commercial General 
Liability and Accident Medical Insurance Program brochure</a>.

<p>For additional information including Enrollment and other related forms and documents, visit the 
<a href="http://www.squaredance.gen.or.us/" title="Click link for Enrollment and other related forms.">Oregon Federation of Square and Round Dance Clubs</a>
website.&nbsp; From the home page, follow the left hand column down to the header 
"Insurance Forms".

