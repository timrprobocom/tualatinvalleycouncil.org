<h2>TVC Recognition Award</h2>

<p>Each year the TVC Recognition Award is presented to an individual, or couple, who has made a significant contribution to the square dancing community.</p>

<p>We are pleased to announce the 2018 TVC Recognition Award goes to 
<B>Judy Schnase</B>.
</p>

<!--img style='padding-right: 5px; float: left' src='images/clendenin.jpg' -->


<p>
Judy is well known to pretty much every individual in the TVC, as well as outside the TVC. She
is very well liked and respected. Judy is a very dedicated dancer, not only by going to most every
TVC dance and every Anniversary dance, but also she makes sure that she drags her fellow members
with her to support the clubs.
<p>
Judy is a huge believer in visitations, and works very hard to get support for this. Judy has set
up many mystery trips over the years, and, as most of you know, this is a huge job, and it takes a lot of
planning and hard work to put these together.
<p>
Judy is dedicated to making dancing fun, and works hard to recruit new dancers and to make
them welcome. If it weren.t for people like Judy, square dance and round dance would not be what it
is today.
<p>
Judy is currently the Vice President of the TVC, and I am very proud and happy to have her as
a partner. For these reasons and many more, myself and the TVC are very happy to recognize Judy
Schnase as the 2018 recipient of the TVC Outstanding Contribution Award.
<p>
January 28, 2019<br>
Mike St. Clair, President

