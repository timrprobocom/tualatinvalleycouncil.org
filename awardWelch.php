<h2>TVC Recognition Award</h2>

<p>&nbsp; Each year the TVC Recognition Award is presented to an individual, or couple, who has made a significant contribution to the square dancing community.</p>

<p>We are pleased to announce the 2012 TVC Recognition Award goes to <B>Bart and Debra Welch</B>.
</p>

<img style='padding-right: 5px; float: left' src='images/welch.jpg'>

<p>Bart and Debra love to dance and have been square and round
dancing for thirteen years.&nbsp; They are enthusiastic dancers and will
be seen on the floor moving to the music, clapping when others dance,
and "whooping it up" to encourage the square.&nbsp; Sometimes known for
their humorous pranks, they are fun to be with and fellow dancers always
love dancing with them!</p>

<p>Bart and Debra are also known for a steadfast work ethic, always
stepping up to lead or help with an organization when needed.&nbsp; They
have held multiple offices for most all of their 13 years of dancing
and there has never been a job that they aren't happy to do.</p>

<p>They not only strengthen the dancing community by example, but are
experts in getting others involved.&nbsp; They are the ultimate leaders,
both at the club and council level. </p>

<p>As past Past President of TVC, Debra believed the strength of TVC was
a reflection of the involvement by all TVC clubs and its members.&nbsp;
Spending hours on the phone texting, talking and making contact with
people to ensure that tasks were distributed and carried out by a
cross-section of TVC club members, she stressed the importance of
club involvement when surveying and discussing issues relevant to the
clubs.</p>

<p>Bart and Debra are fun loving, hard working, and charismatic.</p>

