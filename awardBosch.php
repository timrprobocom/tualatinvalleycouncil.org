<h2>TVC Recognition Award</h2>

<p>Each year since 1994, the TVC Recognition Award is presented to an individual or 
couple who has made a significant contribution to the square dancing community.</p>

<p>We are pleased to announce the 2020 TVC Recognition Award goes to 
<b>Bob &amp; Carolyn Bosch</b>.
</p>

<img style='padding-right: 5px; float: left' src='/files/Bosch-B-C.png'>

<p>
2021 marks the 28th year this award has been given out. In 2020 we were
not able to have our ceremony for this award because of Covid but we
did have a winner. I did not want to present this award without having
a ceremony for them so I decided to wait till we could meet.

<p>
As it turns out they were not able to attend the ceremony but that’s
the way things have gone this last couple of years.

<p>
For the year 2020 Bob and Carolyn Bosch are the winners of this
award. Some of you may not know that they are the ones that started this
award 28 years ago and, in that time, they have been a big part of our
square and round dance lives. If it weren’t for Sq-Dini Badges, we may
not know the persons names across from us in our square and some cases
we would not know who we are.  They have been involved with most all
our major events, working hard at getting us the hardware we need. Along
with supporting our clubs all year long.

<p>
I am very proud to be able to present this award to Bob &amp; Carolyn
Bosch for the year 2020. A year that will go down in the History books.

<p>
Congratulation to Bob &amp; Carolyn Bosch

